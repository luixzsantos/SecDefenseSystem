# Operadores Aritméticos 

# Adição
adicao = 22 + 33
print('Adição:', adicao)

# Subtração
subtracao = 78 - 29
print('Subtração:', subtracao)

# Multiplicação
multiplicacao = 20 * 100
print('Multiplicação:', multiplicacao)

# Divisão
divisao = 100 / 2.8 
print('Divisão:', divisao) # float

# Divisão Inteira 
divisao = 100 // 2.8
print('Divisão Inteira:', divisao)

# Exponenciação
exponeciacao = 2 ** 3
print('Exponenciação:', exponeciacao)

# Módulo
modulo = 55 % 15
print('Módulo:', modulo) # Resto da divisão