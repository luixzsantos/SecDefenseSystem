# Tipos int e float
# int -> número inteiro
# O tipo int representa qualquer número
# Positivo ou negativo. int sem sinal considerado
# Positivo.
print(11) # int
print(-11) # int
print(0)

# Float -> Número com ponto flutuante
# O tipo float representa qualquer número
# Positivo ou negativo com ponto flutuante
# Float sem sinal é considerado positivio
print(11.1, 12.1)
print(0.0, -0.1)

# A função python mostra o tipo que o Python
# inferiu ao valor.
print(type('Otávio'))
print(type(1))
print(type(1.5), type(-1.1), type(0.0))
